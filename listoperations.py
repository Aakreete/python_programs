fruits=['Apple', 'Banana', 'Oranges']
print(fruits)

fruits.insert(1,'Avocado')  #insert
print(fruits)

fruits.sort()          #sort
print(fruits)

fruits.append("Papaya")   #append
print(fruits)

fruits.reverse()           #reverse
print(fruits)

del fruits[1]           #delete
print(fruits)

fruits[2]="grapes"          #replace
print(fruits)
